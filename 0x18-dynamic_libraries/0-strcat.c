/**
*_strcat - Concatenates two strings
*
*@dest: string parameter
*
*@src: string parameter
*
*Return: concatenated string
*/
char *_strcat(char *dest, char *src)
{
	int s, t;

	for (s = 0; *(dest + s) != '\0'; s++)
	{
	}
	for (t = 0; src[t] != '\0'; t++)
	{
		dest[s] = src[t];
		s++;
	}
	dest[s] = '\0';

	return (dest);
}
