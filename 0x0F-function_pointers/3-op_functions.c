#include "3-calc.h"
#include <stdio.h>
/**
*op_add - add 2 integers
*
*@a: integer 1
*@b: integer 2
*
*Return: sum of two integers
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
*op_sub - subtracts 2 integers
*
*@a: integer 1
*@b: integer 2
*
*Return: difference of two integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - multiplies two integers
*
*@a: integer 1
*@b: integer 2
*
*Return: multiplication of two integers
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - divides 2 integers
*
*@a: integer 1
*@b: integer 2
*
*Return: division of two integers
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - modulus 2 integers
*
*@a: integer 1
*@b: integer 2
*
*Return: modulus of two integers
*/

int op_mod(int a, int b)
{
	return (a % b);
}








